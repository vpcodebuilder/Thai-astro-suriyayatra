# tier → (good|warn|neutral) index
TIER_TO_INDEX = {
    "very_high": 0,
    "high":      0,
    "moderate":  2,
    "low":       1,
    "very_low":  1,
}


def get_category_intro(category: str, tier: str, sig: str, bhava: str, rashi: str) -> str | None:
    """คืนบทเปิดเฉพาะ category — เลือกตาม verdict tier (good/warn/neutral)
    คืน None ถ้าไม่มี entry สำหรับ category นี้ (fallback ไป generic)
    """
    entry = CATEGORY_INTROS.get(category)
    if entry is None:
        return None
    idx = TIER_TO_INDEX.get(tier, 2)
    tmpl = entry[idx]
    return tmpl.format(sig=sig, bhava=bhava, rashi=rashi)


__all__ = ["CATEGORY_INTROS", "get_category_intro"]

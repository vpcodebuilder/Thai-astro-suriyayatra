def compute_house_relation(asked_bhava: int, sig_house: int, topic: str = "นี้") -> HouseRelation:
    """คำนวณระยะห่างจากภพคำถามไปถึงภพ significator.

    distance = ((sig_house - asked_bhava) mod 12) + 1   (1-12)
    """
    if not (1 <= asked_bhava <= 12) or not (1 <= sig_house <= 12):
        raise ValueError("bhava ต้องเป็น 1-12")
    distance = ((sig_house - asked_bhava) % 12) + 1
    data = HOUSE_RELATIONS[distance]
    return HouseRelation(
        distance=distance,
        name=data["name"],
        tone=data["tone"],
        score=data["score"],
        text=data["text"].format(topic=topic),
    )



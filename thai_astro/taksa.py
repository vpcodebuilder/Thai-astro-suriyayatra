def _transit_taksa_text(transit_planet: str, bhava, aspect: str) -> Tuple[str, str]:
    """คืน (text, tone) สำหรับดาวจร × ทักษา"""
    base = TRANSIT_PLANET_X_BHAVA.get(
        (transit_planet, bhava.position),
        f"กระทบเรื่อง{bhava.theme}",
    )

    # tone: ใช้คุณสมบัติของดาวจร + tone ของภพ
    SLOW_HEAVY = {"เสาร์", "ราหู"}
    if bhava.tone == "bad":
        tone = "warning"
    elif transit_planet in SLOW_HEAVY:
        tone = "warning" if bhava.position in {2, 5, 7} else "neutral"
    elif transit_planet in {"พฤหัสบดี", "ศุกร์"}:
        tone = "good"
    else:
        tone = "neutral"

    if aspect == "กุม":
        text = (
            f"ดาว{transit_planet}จรกุมดาว{bhava.planet}เดิม "
            f"({bhava.name}) — {base}"
        )
    else:  # เล็ง
        text = (
            f"ดาว{transit_planet}จรเล็งดาว{bhava.planet}เดิม "
            f"({bhava.name}) — มีปะทะ/เปลี่ยนแปลงเรื่องนี้: {base}"
        )
    return text, tone


def transit_aspects_on_taksa(
    taksa: Taksa,
    natal_planets: Dict[str, "object"],
    transit_planets: Optional[Dict[str, "object"]] = None,
) -> List[dict]:
    """หาดาวจรกระทบดาวทักษา (กุม/เล็ง)
    ใช้ตาราง TRANSIT_PLANET_X_BHAVA ที่กระจายตามคุณสมบัติดาวจร
    """
    if not transit_planets:
        return []

    results = []
    for bhava in taksa.bhavas:
        planet = bhava.planet
        if planet not in natal_planets:
            continue
        natal_rasi = natal_planets[planet].zodiac.rasi

        for tname, tplanet in transit_planets.items():
            if tname == planet:
                continue
            diff = (tplanet.zodiac.rasi - natal_rasi) % 12
            if diff == 0:
                aspect = "กุม"
            elif diff == 6:
                aspect = "เล็ง"
            else:
                continue

            note, tone = _transit_taksa_text(tname, bhava, aspect)

            results.append({
                "bhava": bhava.name,
                "bhava_position": bhava.position,
                "natal_planet": planet,
                "transit_planet": tname,
                "aspect": aspect,
                "tone": tone,
                "note": note,
            })
    return results

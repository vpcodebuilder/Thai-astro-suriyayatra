@dataclass
class TransitAspect:
    """ผลกระทบของดาวจร 1 คู่ต่อดาวกำเนิด 1 ดวง"""
    transit_planet: str       # ดาวจร
    natal_planet: str         # ดาวเดิม
    aspect_type: str          # "กุม" หรือ "เล็ง" หรือ "ตรีโกณ" ฯลฯ
    transit_rasi: str         # ราศีของดาวจร
    natal_rasi: str           # ราศีของดาวเดิม
    prediction: str           # คำพยากรณ์
    severity: int             # 1-3 (สูง=กุม, กลาง=เล็ง, ต่ำ=อื่น ๆ)
    duration_note: str        # เช่น "≈ 2.5 ปี/ราศี"


def _aspect_severity(aspect_type: str, transit_planet: str) -> int:
    """ความสำคัญของ aspect (สูง = เด่นชัด)
    ดาวจรเดินช้า + กุม = หนักสุด
    """
    slow = {"เสาร์", "พฤหัสบดี", "ราหู", "เกตุ"}
    base = {"กุม": 3, "เล็ง": 2, "ตรีโกณ": 1, "จัตุโกณ": 1}.get(aspect_type, 0)
    if transit_planet in slow and aspect_type in ("กุม", "เล็ง"):
        base += 1
    return base


def _opposition_modifier(text: str) -> str:
    """ปรับคำทำนาย (กุม) ให้เป็นแบบเล็ง: เน้นการปะทะ-เผชิญหน้า"""
    if not text:
        return text
    return f"{text}  (แบบ \"เล็ง\" — เน้นการปะทะ/เผชิญหน้า บั่นทอนกัน)"


def find_transit_aspects(
    natal_planets: Dict[str, "object"],
    transit_planets: Dict[str, "object"],
    include_minor: bool = False,
) -> List[TransitAspect]:
    """หาคู่ดาวจรกระทบดาวเดิมทั้งหมด

    natal_planets, transit_planets: dict[str, Planet] (จาก thai_astro.planets)
    include_minor: True = รวมตรีโกณ/จัตุโกณ ด้วย

    คืนค่า: list ของ TransitAspect เรียงตาม severity จากสูงไปต่ำ
    """
    from .planets import RASI_NAMES_TH, PLANET_ORDER

    aspects: List[TransitAspect] = []
    for tname in PLANET_ORDER:
        if tname not in transit_planets:
            continue
        t = transit_planets[tname]
        for nname in PLANET_ORDER:
            if nname not in natal_planets:
                continue
            n = natal_planets[nname]
            diff = (t.zodiac.rasi - n.zodiac.rasi) % 12

            if diff == 0:
                aspect_type = "กุม"
                text = CONJUNCTION_TEXT.get((tname, nname), "")
            elif diff == 6:
                aspect_type = "เล็ง"
                text = _opposition_modifier(CONJUNCTION_TEXT.get((tname, nname), ""))
            elif include_minor and diff in (4, 8):
                aspect_type = "ตรีโกณ"
                base = CONJUNCTION_TEXT.get((tname, nname), "")
                text = f"{base}  (แบบ \"ตรีโกณ\" — ส่งเสริมแบบเบา ๆ)" if base else ""
            elif include_minor and diff in (3, 9):
                aspect_type = "จัตุโกณ"
                base = CONJUNCTION_TEXT.get((tname, nname), "")
                text = f"{base}  (แบบ \"จัตุโกณ\" — กระตุ้น/ขัดแย้งเล็กน้อย)" if base else ""
            else:
                continue

            if not text:
                continue

            aspects.append(TransitAspect(
                transit_planet=tname,
                natal_planet=nname,
                aspect_type=aspect_type,
                transit_rasi=RASI_NAMES_TH[t.zodiac.rasi],
                natal_rasi=RASI_NAMES_TH[n.zodiac.rasi],
                prediction=text,
                severity=_aspect_severity(aspect_type, tname),
                duration_note=TRANSIT_DURATION.get(tname, ""),
            ))

    # เรียง: severity สูง -> ต่ำ, แล้วตามลำดับดาวจร (slow first)
    slow_order = ["เสาร์", "ราหู", "พฤหัสบดี", "เกตุ", "อังคาร", "อาทิตย์", "ศุกร์", "พุธ", "จันทร์"]
    rank = {p: i for i, p in enumerate(slow_order)}
    aspects.sort(key=lambda a: (-a.severity, rank.get(a.transit_planet, 99)))
    return aspects


def _short_prediction(text: str) -> str:
    """ตัดคำทำนายเป็นประโยคสั้นพอใช้ในสรุป (ตัดส่วน modifier ออก)"""
    if not text:
        return ""
    # ตัด modifier ("(แบบ ...)" ที่ผูกท้าย)
    if "  (แบบ " in text:
        text = text.split("  (แบบ ")[0]
    # เอาเฉพาะประโยคแรก (ก่อน period หรือ space-dot)
    return text.strip()


def generate_summary(aspects: List[TransitAspect]) -> Dict:
    """สรุปคำทำนายเป็นข้อความบรรยายอ่านง่าย

    คืน dict:
        {
          "headline": str,            # หัวเรื่อง 1 ประโยค
          "top_items": List[dict],    # 3 อันดับแรกพร้อม narrative
          "good_items": List[dict],   # aspect ในด้านดี
          "warn_items": List[dict],   # aspect ที่ต้องระวัง
          "conclusion": str,          # สรุปท้าย
          "tones": dict,              # นับ tone แต่ละประเภท
        }
    """
    important = [a for a in aspects if a.severity >= 2]

    tones = {"good": 0, "heavy": 0, "warning": 0, "neutral": 0}
    classified = []
    for a in important:
        tone = TONE_BY_TRANSIT.get(a.transit_planet, "neutral")
        # ดาวเดิมที่เป็น "ดี" (พฤหัสฯ, ศุกร์) ถ้าโดน เสาร์/ราหู ถือว่าหนักเป็นพิเศษ
        if tone in ("heavy", "warning") and a.natal_planet in ("พฤหัสบดี", "ศุกร์", "อาทิตย์"):
            tone_str = "หนัก"
        elif tone == "good":
            tone_str = "ดี"
        elif tone == "heavy":
            tone_str = "หนัก"
        elif tone == "warning":
            tone_str = "ระวัง"
        else:
            tone_str = "กลาง"
        tones[tone if tone in tones else "neutral"] += 1
        classified.append({
            "aspect": a,
            "tone": tone,
            "tone_label": tone_str,
            "theme": THEME_BY_NATAL.get(a.natal_planet, ""),
            "short": _short_prediction(a.prediction),
        })

    # Top 3 สำคัญที่สุด (เด่นสุด)
    top_items = []
    for i, item in enumerate(classified[:3]):
        a = item["aspect"]
        top_items.append({
            "rank": i + 1,
            "transit_planet": a.transit_planet,
            "natal_planet": a.natal_planet,
            "aspect_type": a.aspect_type,
            "transit_rasi": a.transit_rasi,
            "natal_rasi": a.natal_rasi,
            "tone": item["tone"],
            "tone_label": item["tone_label"],
            "theme": item["theme"],
            "narrative": _build_narrative(a, item["theme"], item["tone_label"]),
            "duration": a.duration_note,
        })

    # ด้านดี / ด้านระวัง (เอาที่ไม่อยู่ใน top 3)
    rest = classified[3:]
    good_items = [
        _make_simple_item(it["aspect"], it["theme"])
        for it in rest if it["tone"] == "good"
    ][:4]
    warn_items = [
        _make_simple_item(it["aspect"], it["theme"])
        for it in rest if it["tone"] in ("heavy", "warning")
    ][:5]

    # Headline + Conclusion
    n_good = tones["good"]
    n_warn = tones["heavy"] + tones["warning"]

    if n_warn >= 3 and n_good == 0:
        headline = "ช่วงนี้เป็นช่วงท้าทาย ต้องอดทนและใช้สติเป็นที่พึ่ง"
        conclusion = (
            "โดยรวม: ระมัดระวังเรื่องที่กล่าวข้างต้นเป็นพิเศษ "
            "อย่าตัดสินใจสำคัญด้วยอารมณ์ พยายามทำบุญ "
            "ดูแลสุขภาพและความสัมพันธ์รอบตัว ผ่านช่วงนี้ไปได้แน่นอน"
        )
    elif n_good >= 3 and n_warn == 0:
        headline = "ช่วงนี้เป็นช่วงเวลาที่ดี มีโอกาสและความเจริญรอคุณอยู่"
        conclusion = (
            "โดยรวม: ใช้โอกาสนี้เริ่มต้นสิ่งใหม่ ๆ ลงทุน ขยายงาน "
            "หรือพัฒนาความสัมพันธ์ที่สำคัญ "
            "ทำบุญเพื่อเสริมดวงให้ดียิ่งขึ้น"
        )
    elif n_good > 0 and n_warn > 0:
        headline = "ช่วงนี้มีทั้งโอกาสและความท้าทายปะปนกัน เป็นช่วงเปลี่ยนแปลงสำคัญ"
        conclusion = (
            "โดยรวม: ใช้โอกาสที่มีเดินหน้าเรื่องที่ดี "
            "พร้อมเตรียมตัวรับมือกับสิ่งที่หนัก ๆ ด้วยสติ "
            "ปรึกษาผู้ใหญ่ที่ไว้ใจได้ และทำบุญเสริมดวงเป็นประจำ"
        )
    elif n_good > 0:
        headline = "ช่วงนี้มีเรื่องดี ๆ เข้ามาในชีวิต"
        conclusion = "โดยรวม: ใช้โอกาสที่มีให้คุ้มค่า"
    elif n_warn > 0:
        headline = "ช่วงนี้มีเรื่องที่ต้องระมัดระวัง"
        conclusion = "โดยรวม: ใช้สติเป็นที่พึ่ง อย่ารีบตัดสินใจสำคัญ"
    else:
        headline = "ช่วงนี้เป็นช่วงปกติ ไม่มีอิทธิพลของดาวจรที่รุนแรง"
        conclusion = "โดยรวม: ใช้ชีวิตตามปกติ ดูแลตัวเองและคนรอบข้าง"

    return {
        "headline": headline,
        "top_items": top_items,
        "good_items": good_items,
        "warn_items": warn_items,
        "conclusion": conclusion,
        "tones": tones,
    }


def _build_narrative(a: TransitAspect, theme: str, tone_label: str) -> str:
    """สร้างประโยคบรรยายสำหรับแต่ละ aspect"""
    short = _short_prediction(a.prediction)
    return (
        f"ดาว{a.transit_planet}ที่กำลังจรในราศี{a.transit_rasi} "
        f"กำลัง{a.aspect_type}ดาว{a.natal_planet}กำเนิดในราศี{a.natal_rasi} "
        f"ส่งผลต่อเรื่อง{theme}: {short}"
    )


def _make_simple_item(a: TransitAspect, theme: str) -> dict:
    return {
        "transit_planet": a.transit_planet,
        "natal_planet": a.natal_planet,
        "aspect_type": a.aspect_type,
        "theme": theme,
        "short": _short_prediction(a.prediction),
        "duration": a.duration_note,
    }

def _bhava_label(b: int) -> str:
    return f"ภพ{b} ({BHAVA_NAMES[b-1]})"


def _classify_pair(lord_bhava: int, located_bhava: int) -> Tuple[str, str]:
    """คืน (tone, tone_label):
        tone ∈ {"good", "warning", "neutral"}
        - good: ภพดีอยู่ภพดี / ทุกข์อยู่ทุกข์ (วิปริต-ราชโยค)
        - warning: ภพดีตกภพทุกข์
        - neutral: อื่น ๆ
    """
    lord_tone = BHAVA_TONE[lord_bhava]
    loc_tone = BHAVA_TONE[located_bhava]

    if lord_tone == "good" and loc_tone == "good":
        return ("good", "ดี")
    if lord_tone == "dusthana" and loc_tone == "dusthana":
        # วิปริต-ราชโยค: ทุกข์ชนทุกข์ = ดี
        return ("good", "ดี (วิปริต)")
    if lord_tone == "good" and loc_tone == "dusthana":
        return ("warning", "ระวัง")
    if lord_tone == "dusthana" and loc_tone == "good":
        # ทุกข์ตกภพดี = นำทุกข์เข้าภพดี (ระวังเล็กน้อย)
        return ("warning", "ปะปน")
    return ("neutral", "กลาง")


def _generic_text(lord_bhava: int, located_bhava: int) -> str:
    """สร้างคำทำนายแบบ template เมื่อไม่มีในตาราง SPECIAL"""
    subj = BHAVA_SUBJECT[lord_bhava]
    arena = BHAVA_ARENA[located_bhava]
    tone, _ = _classify_pair(lord_bhava, located_bhava)

    lord_lbl = BHAVA_NAMES[lord_bhava - 1]
    loc_lbl = BHAVA_NAMES[located_bhava - 1]
    head = f"เจ้าเรือน{lord_lbl}ไปอยู่{loc_lbl}:"

    if tone == "good":
        body = (
            f"{subj} จะเด่นขึ้นและส่งผลดีผ่าน{arena} "
            f"คาดหวังผลดีในเรื่องนี้ได้"
        )
    elif tone == "warning" and BHAVA_TONE[lord_bhava] == "good":
        body = (
            f"{subj} อาจถูกบั่นทอนหรือมีอุปสรรค "
            f"ผ่านปัญหาจาก{arena} ต้องระวังเป็นพิเศษ"
        )
    elif tone == "warning":
        body = (
            f"{subj} ส่งผลปะปนเข้ามาใน{arena} "
            f"มีทั้งดีและเรื่องที่ต้องระวัง"
        )
    else:
        body = (
            f"{subj} จะเชื่อมโยงกับ{arena} "
            f"ใช้โอกาสนี้พัฒนาเรื่องดังกล่าวร่วมกัน"
        )

    return f"{head} {body}"


def text_for_pair(lord_bhava: int, located_bhava: int) -> str:
    """คืนคำทำนายสำหรับ (เจ้าเรือนภพ X, ไปอยู่ภพ Y)
    ใช้ override จาก SPECIAL ก่อน ถ้าไม่มีก็สร้างจาก template
    """
    if (lord_bhava, located_bhava) in SPECIAL_BHAVA_PAIRS:
        return SPECIAL_BHAVA_PAIRS[(lord_bhava, located_bhava)]
    return _generic_text(lord_bhava, located_bhava)



def predict(chart: Chart) -> str:
    lines: List[str] = []
    lines.append("=" * 60)
    lines.append("คำพยากรณ์เบื้องต้น")
    lines.append("=" * 60)

    lord = chart.chart_lord
    if lord in chart.planets:
        p = chart.planets[lord]
        h = chart._rasi_to_house(p.zodiac.rasi)
        lines.append("")
        lines.append(f"[เจ้าชะตา: {lord}]")
        lines.append(
            f"  อยู่ในราศี{RASI_NAMES_TH[p.zodiac.rasi]} บ้านที่ {h} "
            f"({HOUSE_MEANINGS.get(h, '')})"
        )
        lines.append(f"  อิทธิพล: {PLANET_NATURE.get(lord, '')}")
        if h in LORD_IN_HOUSE_NOTES:
            lines.append(f"  {LORD_IN_HOUSE_NOTES[h]}")

    for header, houses in [
        ("[บ้านที่ 1 - ตัวเอง บุคลิก]", [1]),
        ("[บ้านตรีโกณ (1, 5, 9) - มิตรและความสำเร็จ]", [1, 5, 9]),
        ("[บ้านที่ 7 - คู่ครอง]", [7]),
        ("[บ้านที่ 10 - การงาน เกียรติยศ]", [10]),
        ("[บ้านที่ 6 - ศัตรู/โรค]", [6]),
        ("[บ้านที่ 8 - อายุ/อันตราย]", [8]),
    ]:
        lines.append("")
        lines.append(header)
        for h in houses:
            ph = chart.house_planets[h]
            if ph:
                lines.append(f"  บ้าน {h}: {', '.join(ph)}")
            else:
                lines.append(f"  บ้าน {h}: (ไม่มีดาว)")

    lines.append("")
    lines.append("-" * 60)
    lines.append("หมายเหตุ: คำพยากรณ์เป็นเพียงข้อมูลเบื้องต้น")
    return "\n".join(lines)
